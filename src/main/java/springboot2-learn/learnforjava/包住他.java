package learn.learnforjava;

public class 包住他 {
    private final String in;
    private final String haha;

    public 包住他(String in, String haha) {
        this.in = in;
        this.haha = haha;
    }

    public String getIn() {
        return in;
    }

    public String getHaha() {
        return haha;
    }
}
